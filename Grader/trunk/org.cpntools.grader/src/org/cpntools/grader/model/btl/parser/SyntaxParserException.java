package org.cpntools.grader.model.btl.parser;

public class SyntaxParserException extends Exception {

	/**
     * 
     */
	private static final long serialVersionUID = 1L;

	public SyntaxParserException() {
		super();
	}

	public SyntaxParserException(final String arg0) {
		super(arg0);
	}

	public SyntaxParserException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

	public SyntaxParserException(final Throwable arg0) {
		super(arg0);
	}

}
